using Content.Client.Audio;
using Content.Client.UI;
using Robust.Client.AutoGenerated;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client.MainMenu;

[GenerateTypedNameReferences]
public sealed partial class MainMenuHud : Control
{
	[Dependency] private readonly MainMenuAudio _audio = default!;

	private const string TitleArtPath = "/Textures/Interface/titleArt.png";

	public MainMenuHud()
	{
		RobustXamlLoader.Load(this);
		IoCManager.InjectDependencies(this);

		CustomCursor.SetCursor(this);
		_audio.AddButtonSound("pop.wav", 
			ConnectToLocalHostButton,
			ConnectButton,
			OptionsButton,
			QuitButton);

		var sprite = new SpriteSpecifier.Texture(new(TitleArtPath));
		TitleArt.SetFromSpriteSpecifier(sprite);
	}
}